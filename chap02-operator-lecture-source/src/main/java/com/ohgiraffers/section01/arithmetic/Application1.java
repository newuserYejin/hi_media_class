package com.ohgiraffers.section01.arithmetic;

public class Application1 {
    public static void main(String[] args) {

        // 산술연산자

        // 우선순위 *, / -> +, -

        int num1 = 20;
        int num2 = 7;

        System.out.println("num1 + num2 = " + (num1 + num2));
        System.out.println("num1 - num2 = " + (num1 - num2));
        System.out.println("num1 * num2 = " + (num1 * num2));
        System.out.println("num1 / num2 = " + (num1 / num2));
        System.out.println("num1 + num2 = " + (num1 % num2));

    }
}
